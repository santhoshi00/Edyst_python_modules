"""Using the concept of f-strings, print the following statement by substituting the ages:

Riya's age is 5
Mahesh is 10
And their best friend, Arjun, is 12
"""

name="Riya"
age = "5"
print(f"{name}'s age is {age}")
name="Mahesh"
age="10"
print(f"{name} is {age}")
name="Arjun"
age="12"
print(f"And their best friend, {name}, is {age}")
